package com.someli.someli.request;

public class SintegraRequest {
	private String uf;
	private String cnpj;
	private String ie;
	private String ieProdutor;
	private String cpf;

	// Getters e Setters
	public String getUf() {
		return uf;
	}

	public void setUf(String uf) {
		this.uf = uf;
	}

	public String getCnpj() {
		return cnpj;
	}

	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}

	public String getIe() {
		return ie;
	}

	public void setIe(String ie) {
		this.ie = ie;
	}

	public String getIeProdutor() {
		return ieProdutor;
	}

	public void setIeProdutor(String ieProdutor) {
		this.ieProdutor = ieProdutor;
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
}
